package model.dungeonsanddragons;

/**
 * Sound enumeration represents the type of sound that the player can hear to identify near-by
 * pit. CRACKLING sound can be heard by the player when the player is one location away from
 * the pit. Since the user uses hearing to traverse through the dungeon, this enumeration is kept
 * as public.
 */
public enum Sound {
  CRACKLING
}
